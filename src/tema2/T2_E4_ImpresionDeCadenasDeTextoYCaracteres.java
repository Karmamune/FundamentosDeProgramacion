public class T2_E4_ImpresionDeCadenasDeTextoYCaracteres {
    public static void main(String[] args){

        char caracter= 'A';//inicializa el char
        String cadena = "Esta también es una cadena";
        int entero = 1234;//inicializa el entero
        System.out.printf("%c\n", caracter);
        System.out.printf("%s\n", "Esta también es una cadena");
        System.out.printf("%s\n", cadena);
        System.out.printf("%S\n", cadena);
        System.out.printf("%s\n", entero);
    }
}
